#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	len;
	size_t	i;
	char	*ptr;

	i = 0;
	len = ft_strlen (s);
	ptr = (char *)malloc(sizeof(char) * (len + 1));
	if (!s || !f)
		return (NULL);
	if (!ptr)
		return (NULL);
	while (i < len)
	{
		ptr[i] = f(i, s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
